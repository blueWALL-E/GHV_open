%Get_Aerodynamic 计算GHV的气动参数
%input
% air_ang 单位 弧度 气流角 alpha beta
% w     单位 rad/s飞行器转动角速度
% v     单位 m/s 飞行器速度标量
% M     单位 n.d. 马赫数
% GHV_cfg 飞行器基本参数结构体

%output
% Coef_F     单位n.d.气动参数矩阵(仿射部分)  C = [CD; CY; CL; Cl; Cm; Cn];
% 阻力系数 侧向力系数 升力系数 滚转力矩系数 俯仰力矩系数 偏航力矩系数
%note
% 在这里简单定义为气动系数仿射部分为与舵面无关的项
function Coef_F = Aerodynamic_coefficients_affine(air_ang, v, w, M, GHV_cfg)
    %输出变量大小定义
    Coef_F = zeros(6, 1);

    % 已对整个气动参数代码格式进行调整 原格式没有可读性
    % _iu表示英制单位 没有下标表示标准单位制
    % 内部计算使用了一部分英制单位参与计算，请一定注意单位制之间的不同
    % 在马赫数分段点，气动参数并不连续，在这里人为0阶连续处理
    % 气动参数并非特别理想，当飞行器攻角大于12度 小于0度 马赫数大于24时 参数失效
    % 在不施加控制率的情况下 因攻角过大 开环仿真结果与实际情况有较大差距
    % 希望有一天能用上中国自己的高超声速飞行器开源模型

    %匹配 AIAA 命名规则 文献 dio 10.2514/6.2007-6626
    p = w(1, 1); %单位 rad/s 机体旋转角速度在x轴方向的分量 滚转角速度
    q = w(2, 1); %单位 rad/s 机体旋转角速度在y轴方向的分量 偏航角速度
    r = w(3, 1); %单位 rad/s 机体旋转角速度在z轴方向的分量 俯仰角速度
    alpha = air_ang(1, 1); %读取攻角数据
    beta = air_ang(2, 1); %读取侧滑角数据
    ALPHA = rad2deg(alpha); %转化为角度
    BETA = beta; %保持弧度不变

    %以下数据赖在AIAA 文献 dio 10.2514/6.2007-6626

    %速度饱和检查
    if (M < 0)
        error('error:M<0');
    end

    if (M > 24)
        error('error:M>24');
    end

    b = GHV_cfg.b_ref; %单位 m 机翼展长
    c = GHV_cfg.c_ref; %单位 m 机翼平均弦长
    %计算并输出气动参数
    % 0阶连续处理
    if (M < 1.15)
        C_F = low_speed(M, ALPHA, BETA, p, q, r, v, b, c); % M < 1.25
    elseif (M < 1.35)
        C_F = ((1.35 - M) / 0.2) * low_speed(M, ALPHA, BETA, p, q, r, v, b, c) ...
            + ((M - 1.15) / 0.2) * medium_speed(M, ALPHA, BETA, p, q, r, v, b, c);
    elseif (M < 3.9)
        C_F = medium_speed(M, ALPHA, BETA, p, q, r, v, b, c); % 1.25 < m < 4
    elseif (M < 4.1)
        C_F = ((4.1 - M) / 0.2) * medium_speed(M, ALPHA, BETA, p, q, r, v, b, c) ...
            + ((M - 3.9) / 0.2) * high_speed(M, ALPHA, BETA, p, q, r, v, b, c);
    else
        C_F = high_speed(M, ALPHA, BETA, p, q, r, v, b, c); % M > 4
    end

    Coef_F = reshape(C_F, 6, 1); %转化为列向量

end

